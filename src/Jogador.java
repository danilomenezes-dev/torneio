public class Jogador {
    private String nomeDoJogador;
    private int idadeDoJogador;
    private String posicaoDojogador;
    private Time timeDoJogador;

    public String getNomeDoJogador() {
        return nomeDoJogador;
    }

    public void setNomeDoJogador(String nomeDoJogador) {
        this.nomeDoJogador = nomeDoJogador;
    }

    public int getIdadeDoJogador() {
        return idadeDoJogador;
    }

    public void setIdadeDoJogador(int idadeDoJogador) {
        this.idadeDoJogador = idadeDoJogador;
    }

    public Time getTimeDoJogador() {
        return timeDoJogador;
    }

    public void setTimeDoJogador(Time timeDoJogador) {
        this.timeDoJogador = timeDoJogador;
    }

    public String getPosicaoDojogador() {
        return posicaoDojogador;
    }

    public void setPosicaoDojogador(String posicaoDojogador) {
        this.posicaoDojogador = posicaoDojogador;
    }
}
